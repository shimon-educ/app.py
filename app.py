import streamlit as st
import sympy as sp
import numpy as np
import plotly.graph_objects as go

# 专转 注
st.set_page_config(page_title="MathBuddy", layout="centered")

st.title("И 注转 拽专 砖 砖注")
st.write(" 拽专 转 驻拽爪 爪注 专 爪注.")

# 驻拽爪转 注专 注爪 住驻专
def format_num(n):
    try:
        n_float = float(n)
        return int(n_float) if n_float.is_integer() else round(n_float, 2)
    except:
        return n

# 转 驻拽爪
input_func = st.sidebar.text_input(" 驻拽爪 拽专:", "x**2 / (x**2 + 2*x - 3)")

if input_func:
    x = sp.symbols('x')
    try:
        f = sp.sympify(input_func)
        num, den = sp.fraction(f)
        true_domain = sp.solve(den, x)
        
        # 转 驻转专转 拽
        true_pts = sorted([format_num(p.evalf()) for p in true_domain])
        true_pts_str = ", ".join([str(p) for p in true_pts])
        
        # --- 砖 1: 转 专 ---
        st.header("砖 1: 转 专")
        st.latex(r"f(x) = " + sp.latex(f))

        with st.expander("  爪 转 专? (住专 转专)"):
            st.write("""
            **   转 专?**
            转拽, 住专 拽 驻住.  注 爪  注专 x 驻住 转  爪 转 转.
            **砖:** 砖 转  驻住 ($  = 0 $) 驻转专 转 砖.
            """)
        
        user_input = st.text_input(" 注专 砖驻住 转 ? (砖: 3, 1-)")
        
        if user_input:
            #  拽 砖 注爪专 转 -   砖  砖 
            st.write("拽 转拽: " + user_input)

    except Exception as e:
        st.error("拽 砖 驻注 驻拽爪.  砖转 转  (砖 x**2).")
